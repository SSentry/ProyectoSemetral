package com.example.proyectosemetral;

// CREO UNA CLASE LLAMADA Sandwich con las caracteristicas definidas de los Sadwiches
public class Sandwich {
    public int cod;
    public String nombre;
    public String Descripcion;
    public String Precio;
    public String identificador_imagen;

    public Sandwich(int cod , String nombre , String Descripcion ,String Precio, String identificador_imagen)
    {
        this.cod=cod;
        this.nombre=nombre;
        this.Descripcion=Descripcion;
        this.Precio=Precio;
        this.identificador_imagen=identificador_imagen;

    }



}
